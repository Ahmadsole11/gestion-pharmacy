     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'unite' => 'required|string|min:2',
           
        ]);
        
        $unite = Option::firstOrcreate(
            ['name' =>$request->unite],
            ['unite' => true]
        );

        return response()->json($unite);
    }

